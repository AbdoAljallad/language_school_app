        def populate_chats():
            cb = getattr(self, "populate_chats_callback", None)
            if callable(cb):
                try:
                    cb()
                except Exception:
                    pass
                return
            self.chatsList.clear()
            if not self.current_user_id:
                self.chatsList.addItem("No user selected")
                return
            try:
                rows = Chat.get_chats(self.current_user_id) or []
            except Exception:
                rows = []
            for r in rows:
                name = f"{r.get('first_name') or ''} {r.get('last_name') or ''}".strip() or r.get('other_username') or f"User {r.get('other_user_id')}"
                last = (r.get('last_message') or "")[:60]
                label = f"{name} - {last}"
                if int(r.get('unread_count') or 0):
                    label = f"[{r.get('unread_count')}] {label}"
                it = QtWidgets.QListWidgetItem(label)
                it.setData(QtCore.Qt.UserRole, r.get('chat_id'))
                self.chatsList.addItem(it)
        self.populate_chats = populate_chats

        def populate_messages(chat_id):
            cb = getattr(self, "populate_messages_callback", None)
            if callable(cb):
                try:
                    cb(chat_id)
                except Exception:
                    pass
                return
            self.messagesList.clear()
            if not chat_id:
                self.messagesList.addItem("Select a chat")
                return
            try:
                msgs = Chat.get_messages(chat_id) or []
            except Exception:
                msgs = []
            for m in msgs:
                sender = m.get('sender_id')
                text = m.get('message') or ''
                sent_at = m.get('sent_at') or ''
                # try to resolve sender name
                try:
                    u = User.get_by_id(sender)
                    sender_name = f"{u.first_name} {u.last_name}".strip() if u else str(sender)
                except Exception:
                    sender_name = str(sender)
                # indicate edited/deleted
                suffix = ""
                if m.get('deleted'):
                    suffix = " [deleted]"
                    display_text = f"{sender_name}: [deleted] ({m.get('deleted_at')})"
                else:
                    if m.get('edited'):
                        suffix = " (edited)"
                    display_text = f"{sender_name}: {text}{suffix} ({sent_at})"
                self.messagesList.addItem(display_text)
        self.populate_messages = populate_messages

        def _on_chat_selected():
            item = self.chatsList.currentItem()
            if not item:
                return
            cid = item.data(QtCore.Qt.UserRole)
            try:
                populate_messages(cid)
            except Exception:
                pass
        self.chatsList.itemSelectionChanged.connect(_on_chat_selected)

        def send_message_wrapper():
            item = self.chatsList.currentItem()
            if not item:
                QtWidgets.QMessageBox.warning(None, "Send Message", "Select a chat first.")
                return
            chat_id = item.data(QtCore.Qt.UserRole)
            text = self.messageInput.text().strip()
            if not text:
                return
            try:
                ok = Chat.send_message(chat_id, self.current_user_id, text)
            except Exception:
                ok = False
            if ok:
                self.messageInput.clear()
                try:
                    populate_messages(chat_id)
                    populate_chats()
                except Exception:
                    pass
            else:
                QtWidgets.QMessageBox.warning(None, "Send Message", "Failed to send message.")
        self.sendMessageButton.clicked.connect(send_message_wrapper)
        self.send_message = send_message_wrapper

        def new_chat_wrapper():
            from PyQt5.QtWidgets import QInputDialog, QMessageBox
            username, ok = QInputDialog.getText(None, "New Chat", "Enter username to start chat:")
            if not ok or not username.strip():
                return
            other = User.get_by_username(username.strip())
            if not other:
                QMessageBox.warning(None, "New Chat", "User not found.")
                return
            try:
                cid = Chat.get_or_create_chat(self.current_user_id, other.user_id)
            except Exception:
                cid = None
            if cid:
                populate_chats()
                # select created chat
                for i in range(self.chatsList.count()):
                    it = self.chatsList.item(i)
                    if it.data(QtCore.Qt.UserRole) == cid:
                        self.chatsList.setCurrentRow(i)
                        break
        self.newChatButton.clicked.connect(new_chat_wrapper)
        self.new_chat = new_chat_wrapper

